module.exports = {

  attributes: {

    //  ╔═╗╦═╗╦╔╦╗╦╔╦╗╦╦  ╦╔═╗╔═╗
    //  ╠═╝╠╦╝║║║║║ ║ ║╚╗╔╝║╣ ╚═╗
    //  ╩  ╩╚═╩╩ ╩╩ ╩ ╩ ╚╝ ╚═╝╚═╝
    name: 'string',
    role: {
      type: 'string',
      isIn: ['customer', 'admin', 'chef']
    },
    email: {
      type: 'string',
      isEmail: true,
      required: true,
    },
    password: {
      type: 'string',
      required: true
    },

    //  ╔═╗╔╦╗╔╗ ╔═╗╔╦╗╔═╗
    //  ║╣ ║║║╠╩╗║╣  ║║╚═╗
    //  ╚═╝╩ ╩╚═╝╚═╝═╩╝╚═╝


    //  ╔═╗╔═╗╔═╗╔═╗╔═╗╦╔═╗╔╦╗╦╔═╗╔╗╔╔═╗
    //  ╠═╣╚═╗╚═╗║ ║║  ║╠═╣ ║ ║║ ║║║║╚═╗
    //  ╩ ╩╚═╝╚═╝╚═╝╚═╝╩╩ ╩ ╩ ╩╚═╝╝╚╝╚═╝

    // for customers
    orders: {
      collection: 'order',
      via: 'customer',
    },

    // for chef
    cooking: {
      collection: 'order',
      via: 'chef',
    },
  },

  beforeCreate: async (valueToSet, proceed) => {
    let hash = await sails.helpers.password.encrypt(valueToSet.password)
      .intercept('hashError', () => {
        throw new Error('hashing error');
      });

    valueToSet.password = hash;

    return proceed();
  }

};

